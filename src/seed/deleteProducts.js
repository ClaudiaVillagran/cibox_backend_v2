export const deleteProducts = async () => {
  logger.info(
    { total: productsToDelete.length },
    "delete.products.started",
  );

  let deleted = 0;
  let failed = 0;

  for (const name of productsToDelete) {
    try {
      const result = await Product.deleteMany({ name });

      deleted += result.deletedCount || 0;

      logger.debug(
        { name, deleted: result.deletedCount },
        "delete.products.item_deleted",
      );
    } catch (err) {
      failed += 1;

      logger.error(
        { err: { message: err.message }, name },
        "delete.products.item_failed",
      );
    }
  }

  logger.info(
    {
      requested: productsToDelete.length,
      deleted,
      failed,
    },
    "delete.products.completed",
  );

  return {
    requested: productsToDelete.length,
    deleted,
    failed,
  };
};

export default deleteProducts;